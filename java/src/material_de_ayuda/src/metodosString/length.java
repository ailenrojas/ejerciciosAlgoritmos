package metodosString;

public class length {
	public static void main(String[] args) {

		// declara la cadena como un objeto S1 S2
		String S1 = "arbol";
		String S2 = "computacion";

		// El método length () de String devuelve la longitud de la cadena S1 y S2.
		System.out.println("La Primera cadena tiene: " + S1.length() + " caracteres");
		// Longitud de una cadena 
		System.out.println("La Segunda cadena tiene: " + S2.length() + " caracteres");
	}
}
